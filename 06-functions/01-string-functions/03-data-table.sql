-- USE this functions
SELECT NAME,
       UPPER(NAME) AS UPPER_NAME,
		 LOWER(NAME) AS LOWER_NAME,
		 INITCAP(NAME) AS INITCAP_NAME
FROM STRING_FUNCTIONS_DATA_TABLE;

UPDATE STRING_FUNCTIONS_DATA_TABLE
SET NAME = 'alice johnson'
WHERE ID = 1;

-- email format validation
SELECT NAME, PHONE,
		 CASE
			  WHEN REGEXP_LIKE(PHONE, '^\d{3}-\d{3}-\d{4}$') THEN 'Valid'
			  ELSE 'Invalid'
		 END AS PHONE_VALIDATION
FROM STRING_FUNCTIONS_DATA_TABLE;

