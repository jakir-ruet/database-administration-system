-- Create a data table for string functions examples
CREATE TABLE STRING_FUNCTIONS_DATA_TABLE (
	 ID NUMBER PRIMARY KEY,
	 NAME VARCHAR2(50),
	 SALARY NUMBER,
	 PHONE VARCHAR2(20)
);

