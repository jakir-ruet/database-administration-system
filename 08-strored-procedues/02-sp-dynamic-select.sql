-- Create a table named CUSTOMERS
CREATE TABLE CUSTOMERS (
	 ID INT NOT NULL PRIMARY KEY,
	 NAME VARCHAR(255) NOT NULL,
	 AGE INT NOT NULL,
	 ADDRESS VARCHAR(255),
	 SALARY DECIMAL(18, 2)
);

