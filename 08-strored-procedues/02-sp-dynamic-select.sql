-- SELECT INTO clause
DECLARE
C_ID CUSTOMERS.ID%TYPE := 2;
C_NAME CUSTOMERS.NAME%TYPE;
C_AGE CUSTOMERS.AGE%TYPE;
C_ADDRESS CUSTOMERS.ADDRESS%TYPE;
C_SALARY CUSTOMERS.SALARY%TYPE;
BEGIN
SELECT NAME, AGE, ADDRESS, SALARY INTO C_NAME, C_AGE, C_ADDRESS, C_SALARY
FROM CUSTOMERS
WHERE ID = C_ID;
DBMS_OUTPUT.PUT_LINE('Customer Name: ' || C_NAME);
DBMS_OUTPUT.PUT_LINE('Customer Age: ' || C_AGE);
DBMS_OUTPUT.PUT_LINE('Customer Address: ' || C_ADDRESS);
DBMS_OUTPUT.PUT_LINE('Customer Salary: ' || C_SALARY);
END;
